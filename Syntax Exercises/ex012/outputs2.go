package main
import ("fmt")

func main() {
	var i, j string = "Hello", "World"

	fmt.Println(i, j)
}

/*
	A função Println() em Go:
		- a função "Println()" é semelhante a "Print()", com a diferença que um espaço em branco é adicionado entre os argumentos e uma nova linha é adicionada no final (ex: line 7).
*/